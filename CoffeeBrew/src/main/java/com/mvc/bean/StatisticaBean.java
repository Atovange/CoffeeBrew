package com.mvc.bean;

public class StatisticaBean {
	private int idStatistica;
	private String tipoProdotto;
	private int qtaVendute;
	
	public int getIdStatistica() {
		return idStatistica;
	}
	public void setIdStatistica(int idStatistica) {
		this.idStatistica = idStatistica;
	}
	public String getTipoProdotto() {
		return tipoProdotto;
	}
	public void setTipoProdotto(String tipoProdotto) {
		this.tipoProdotto = tipoProdotto;
	}
	public int getQtaVendute() {
		return qtaVendute;
	}
	public void setQtaVendute(int qtaVendute) {
		this.qtaVendute = qtaVendute;
	}
}
